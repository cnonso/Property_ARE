    internal sealed class Configuration : DbMigrationsConfiguration<PropertyManagerWeb.Models.ApplicationDbContext>
    {
        public Configuration()
        {
            AutomaticMigrationsEnabled = true;
        }

        protected override void Seed(PropertyManagerWeb.Models.ApplicationDbContext context)
        {
            var userStore = new UserStore<ApplicationUser>(context);
            var userManager = new UserManager<ApplicationUser>(userStore);

            if (!context.Users.Any(u => u.UserName == "Administrator"))
            {
                var user = new ApplicationUser { UserName = "Administrator" };
                IdentityResult result = userManager.Create(user, "p@55W0rd!");

                context.Roles.AddOrUpdate(r => r.Name, new IdentityRole { Name = "Admin" });
                context.SaveChanges();

                userManager.AddToRole(user.Id, "Admin");
                context.SaveChanges();
            }
            //  This method will be called after migrating to the latest version.

